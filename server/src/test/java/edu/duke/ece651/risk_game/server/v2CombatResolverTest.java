package edu.duke.ece651.risk_game.server;
import edu.duke.ece651.risk_game.shared.v2CombatResolver;
import org.junit.jupiter.api.Test;
import static org.junit.jupiter.api.Assertions.assertEquals;
public class v2CombatResolverTest {
   @Test
   public void test_CombatResolver() {
       v2CombatResolver combatResolver = new v2CombatResolver();
       int attackNum = 3;
       int defendNum = 2;
       int result = combatResolver.resolveCombat(attackNum, defendNum);
       assertEquals(result, 1);
       assertEquals(combatResolver.resolveCombat(118, 20), 98);
       assertEquals(combatResolver.resolveCombat(2, 100), -98);

   }

}
